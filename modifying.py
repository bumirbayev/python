# Lists

def square_list(nums):
    i = 0
    while i < len(nums):
        nums[i] = nums[i] * nums[i]
        i += 1
        return nums

# Return the sum of all the numbers in the list

def sum_list(nums):
    total = 0
    i =00
    while i < len(nums):
        total[i] += nums[i]
        i += 1
    return total
